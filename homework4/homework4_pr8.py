# Запрашиваем у пользователя ввод двух чисел
a = int(input("Введите первое число (a): "))
b = int(input("Введите второе число (b): "))

# Находим минимум и максимум для корректного диапазона
start = min(a, b) + 1  # Начинаем с следующего числа после меньшего
end = max(a, b)  # Конечное значение

# Выводим все целые числа между a и b
print(f"Целые числа между {a} и {b}:")
for number in range(start, end):
    print(number)
