# Рисуем верхнюю сторону квадрата
print('*' * 10)
    
# Рисуем боковые стороны квадрата
for i in range(8):  # 8 строк между верхней и нижней сторонами
    print('*' + ' ' * 8 + '*')
    
# Рисуем нижнюю сторону квадрата
print('*' * 10)
    
print()  # Печатаем пустую строку для разделения квадратов
